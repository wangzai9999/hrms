package com.util;

public class UserinfoParam {

    private String name;
    private Long ro_id;
    private Long de_id;
    private Long pos_id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getRo_id() {
        return ro_id;
    }

    public void setRo_id(Long ro_id) {
        this.ro_id = ro_id;
    }

    public Long getDe_id() {
        return de_id;
    }

    public void setDe_id(Long de_id) {
        this.de_id = de_id;
    }

    public Long getPos_id() {
        return pos_id;
    }

    public void setPos_id(Long pos_id) {
        this.pos_id = pos_id;
    }
}
